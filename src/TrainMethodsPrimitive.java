public class TrainMethodsPrimitive {

    public void printInt(int x) {
        System.out.println("я получил на вход число " + x);
    }

    public void printIntLong(long xxxx) {
        System.out.println("я получил на вход длинное число " + xxxx);
    }

    public void printChar(char ch) {
        System.out.println("я получил на вход число " + ch);
    }

    public void printFloat(float q) {
        System.out.println("я получил на вход дробное число " + q);
    }

    public void printDouble(double d) {
        System.out.println("Я получил на вход длинное дробное число " + d);
    }

    public void printShort(short s) {
        System.out.println("Я получил на вход короткое число " + s);
    }

    public void printByte(byte b) {
        System.out.println("Я получил на вход очень короткое число " + b);
    }

    public void printBoolean(boolean o) {
        System.out.println("Я получил на вход булево число " + o);
    }
}
